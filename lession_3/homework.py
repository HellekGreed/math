from math import factorial
# 1.
# Даны значения зарплат из выборки выпускников: 100, 80, 75, 77, 89, 33, 45, 25, 65, 17, 30, 24, 57, 55, 70, 75, 65, 84, 90, 150.
# Посчитать среднее арифметическое, среднее квадратичное отклонение, смещенную и несмещенную оценки дисперсий для данной выборки.

salaries = [100, 80, 75, 77, 89, 33, 45, 25, 65, 17, 30, 24, 57, 55, 70, 75, 65, 84, 90, 150]
aver_salaries = sum(salaries) / len(salaries)
print(aver_salaries)

var = 0
for i in range(len(salaries)):
    var += (salaries[i] - aver_salaries)**2

Standard_deviation = (var / len(salaries))**0.5
print(round(Standard_deviation, 2))

shifted_variance = var / len(salaries)
print(shifted_variance)

not_shifted_variance = var / (len(salaries) - 1)
print(round(not_shifted_variance, 2))

