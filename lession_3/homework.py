two_detail = (p1 * p2 * q3) + (p1 * q2 * p3) + (q1 * p2 * p3)
print(round(two_detail, 3))


# в). хотя бы одна деталь
at_least_one_detail = 1 - (q1 * q2 * q3)
print(round(at_least_one_detail, 2))

# г). от одной до двух деталей?
one_detail = (p1 * q2 * q3) + (q1 * p2 * q3) + (q1 * q2 * p3)
